package com.belajar;

public class java17IfelseIfStatment {
    public static void main(String[] args) {
        int a=5;
        System.out.println("Nilai = "+a);
        if (a==10){
            System.out.println("INI IF");
        }
        else if (a==5){
            System.out.println("INI ELSE IF");
        }
        else {
            System.out.println("INI ELSE");
        }
    }
}
