package com.belajar;

public class java4Variabel {
    public static void main(String[] args) {
        //variabel adalah tempat untuk menyimpan data
        //di java ada yang namanya tipe data
        //tipe data adalah tipe dari variabel
        //ada integer, double dll
        //contoh
        int x=10;
        System.out.println("Nilai x ="+ x ) ;
        //x bisa dirubah sesuai dengan urutan program
        x=20;
        System.out.println("Nilai x ="+ x) ;
        //untuk pembuatan variabel tidak boleh menggunakan
        // nama yang sudah ada dalam default java
        //selanjutnya membuat deklarasi pada java contohnya class atau void
        int b; //deklarasi
        b=7; //ini adalah nilainya
        System.out.println(b);
    }
}
