package com.belajar;

public class java9OperatorAssigment {
    public static void main(String[] args) {
        //operator assignment
        //contoh
        int a=1;
        a=a+5;
        System.out.println("Nilai a ="+a);

        //sebenarnya bisa lebih mudah
        //assigment operator jumlah
        a+=5;
        System.out.println("Nilai a baru = "+a);
        //pengurangan
        a-=5;
        System.out.println("Nilai a paling baru = "+a);
        //sisanya liat di w3school
    }
}
